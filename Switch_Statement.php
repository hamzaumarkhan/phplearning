<?php
// Switch Statement.. 
$fruit_name  = 'Banana';
switch ($fruit_name) {
    case 'Apple' : echo 'This is an apple wich is a red apple';
    break;
    case 'Orange' : echo 'This is an Orange';
    break;
    case 'Banana' : echo 'This ia a banana';
    break;
    default: echo 'This is the default value of switch statement wich means no case is perfectly matching with the variable';
}



?>